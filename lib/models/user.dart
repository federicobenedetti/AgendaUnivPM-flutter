class User {
  final String id;
  final String matricola;
  final int phoneNumber;
  final int courseYear;
  final String displayName;
  final String email;
  final String photoUrl;
  final bool situazioneTasse;

  User(this.id,
      this.matricola,
      this.phoneNumber,
      this.courseYear,
      this.displayName,
      this.email,
      this.photoUrl,
      this.situazioneTasse);
}